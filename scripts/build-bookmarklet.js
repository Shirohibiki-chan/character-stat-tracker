// Minifies the bookmarklet source and writes the javascript: URL to a constants file.
// Run via: npm run build:bookmarklet
// Also runs automatically before npm run build (see "prebuild" in package.json).

import { minify } from 'terser'
import { readFileSync, writeFileSync } from 'fs'

const src  = readFileSync('userscript/charsnap-capture.bookmarklet.js', 'utf8')
const result = await minify(src, {
  compress: { passes: 2 },
  mangle:   true,
  format:   { comments: false },
})

if (result.error) {
  console.error('Minification failed:', result.error)
  process.exit(1)
}

const url = 'javascript:' + result.code

const out = [
  '// Auto-generated by scripts/build-bookmarklet.js — do not edit manually.',
  '// Re-generate with: npm run build:bookmarklet',
  `export const BOOKMARKLET_URL = ${JSON.stringify(url)}`,
  '',
].join('\n')

writeFileSync('src/constants/bookmarklet-url.js', out)
console.log(`Bookmarklet URL written (${url.length} chars)`)
